package switchStatement;

public class SwitchStateEx1 {

	public static void main(String args [])
	{
		int r= 4;
		
	switch (r)
	{
	case 1:System.out.println(77);
	break;
	case 2:System.out.println(45);
	break;
	case 3:System.out.println(56);
	break;
	case 4:System.out.println(777);
	break;
	default : System.out.println("The number is not from provided cases");}
	
	}

}
